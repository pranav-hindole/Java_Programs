package basicPrograms;

public class NumberPattern1 {

	public static void main(String[] args) {
		//to print rows
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
		{
		 System.out.print(i);		
		}
			System.out.println();
		}
		//to print columns
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
		{
		 System.out.print(j);		
		}
			System.out.println();
		}
		

	}

}
